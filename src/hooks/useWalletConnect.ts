const IREC_ADDRESS = "0x948b36189BF017b4f4C8335172b497Fad2E8C903";
const VOLT_ADDRESS = "0xd25fdf4f89Fcb3385bf90841cB4bc73FCa360D52";

export const useWalletConnect = () => {
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [activeCerts, setActiveCerts] = useState("0");
  const [tokenCount, setTokenCount] = useState("0");

  const initWeb3Modal = async () => {
    const providerOptions = {};

    web3Modal = new Web3Modal({
      network: "testnet", // optional
      cacheProvider: false, // optional
      providerOptions // required
    });

    if (window.ethereum) {
      web3 = new Web3(window.ethereum as any);
  } else if (window.web3) {
      web3 = new Web3(window.web3.currentProvider);
    };

    const accounts = await web3.eth.getAccounts();

    if (!!accounts.length) {
      setIsLoggedIn(true);
    } else {
      setIsLoggedIn(false);
    }
  }

  const initSmartContract = async () => {
    contract = new web3.eth.Contract(IREC_ABI, IREC_ADDRESS);

    const events = await contract.getPastEvents("Transfer", { fromBlock: 0, toBlock: "latest" });

    const activeCerts = events.reduce((prevValue: any, currentValue: any) => {
      return prevValue + Number(currentValue.returnValues.value)
    }, 0);

    setActiveCerts(activeCerts);

    contract.methods.totalSupply().call(function (err: any, res: any) {
      if (err) {
        console.log("An error occured", err);
        return;
      }

      console.log("The balance is: ", res);
    })
  }

  const initVoltContract = async () => {
    contract = new web3.eth.Contract(VOLT_ABI, VOLT_ADDRESS);

    contract.methods.totalSupply().call(function (err: any, res: any) {
      if (err) {
        console.log("An error occured", err);
        return;
      }

      setTokenCount(res);
    })
  }

  const openModal = async () => {
    provider = await web3Modal.connect();

    web3.setProvider(provider);
  }

  useEffect(() => {
    initWeb3Modal();
    initSmartContract();
    initVoltContract();
  }, []);

  return {
    provider,
    openModal,
    isLoggedIn,
    activeCerts,
    web3,
    contract,
    tokenCount
  };
};
